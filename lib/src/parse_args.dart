Map<String, List<String>> parseArgs(List<String> args) {
  final argsMap = <String, List<String>>{};
  String? currentKey;

  for (final arg in args) {
    if (arg.startsWith('-')) {
      currentKey = arg;
      argsMap[currentKey] = [];
    } else if (currentKey != null) {
      argsMap[currentKey]!.add(arg);
    }
  }

  return argsMap;
}
