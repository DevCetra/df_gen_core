import 'package:df_collection/df_collection.dart';
import 'package:path/path.dart' as p;

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

/// Takes the last 3 segments of the [path].
String previewPath(String path) {
  return '**${p.joinAll(p.split(path).takeLast(3))}';
}
